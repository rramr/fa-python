# При помощи функций map/filter/reduce из списка списков извлечь элементы, содержащиеся во вложенных списках по индексу 1. 
# Например, [[1, 2, 3], [2, 3, 4], [0, 1 , 1 , 1], [0, 0]] -> [2, 3, 1, 0]

def sort(elem):
    return elem[1]

lst = [[1, 2, 3], [2, 3, 4], [0, 1 , 1 , 1], [0, 0]]
lst = list(map(sort, lst))

print(lst)
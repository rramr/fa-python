def calc(*num):
    if len(num) == 1:
        return 0
    if len(num) == 2:
        if num[1] == 0:
            return ["*"]
        else:
            return -num[1]/num[0]
    if len(num) == 3:
        a = num[0]
        b = num[1]
        c = num[2]
        d = pow(b, 2) - 4 * a * c
        x1 = (- b + pow(d, 1/2)) / (2 * a)
        x2 = (- b - pow(d, 1/2)) / (2 * a)
        return [x1, x2]
    return

